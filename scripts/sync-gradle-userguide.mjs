#!/usr/bin/env node

import { promises as fs } from "node:fs";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const OFFICIAL_USERGUIDE_ROOT = "https://docs.gradle.org/current/userguide";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const repoRoot = path.resolve(__dirname, "..");
const skillRoot = path.join(repoRoot, "skills", "gradle");
const referencesRoot = path.join(skillRoot, "references");
const mirrorRoot = path.join(referencesRoot, "userguide");
const remoteLocalMapPath = path.join(referencesRoot, "remote-local-map.md");

const sourceRootArg = process.argv[2];

function usage() {
  const scriptPath = path.relative(process.cwd(), __filename) || __filename;
  console.error(
    `Usage: node ${scriptPath} /absolute/path/to/gradle/platforms/documentation/docs/src/docs/userguide`,
  );
}

function toPosixPath(filePath) {
  return filePath.split(path.sep).join(path.posix.sep);
}

function comparePaths(a, b) {
  return a.localeCompare(b, "en");
}

async function assertDirectoryExists(directoryPath) {
  let stat;
  try {
    stat = await fs.stat(directoryPath);
  } catch {
    throw new Error(`Source directory does not exist: ${directoryPath}`);
  }

  if (!stat.isDirectory()) {
    throw new Error(`Source path is not a directory: ${directoryPath}`);
  }
}

async function walkAdocFiles(rootPath, currentPath = rootPath) {
  const entries = await fs.readdir(currentPath, { withFileTypes: true });
  const files = [];

  for (const entry of entries.sort((a, b) => comparePaths(a.name, b.name))) {
    const entryPath = path.join(currentPath, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await walkAdocFiles(rootPath, entryPath)));
      continue;
    }

    if (entry.isFile() && entry.name.endsWith(".adoc")) {
      files.push(path.relative(rootPath, entryPath));
    }
  }

  return files;
}

function groupByTopLevel(relativePaths) {
  const groups = new Map();

  for (const relativePath of relativePaths) {
    const parts = relativePath.split(path.posix.sep);
    const groupName = parts[0] || "(root)";

    if (!groups.has(groupName)) {
      groups.set(groupName, []);
    }

    groups.get(groupName).push(relativePath);
  }

  return [...groups.entries()].sort(([a], [b]) => comparePaths(a, b));
}

function collectStemCollisions(relativePaths) {
  const stemToPaths = new Map();

  for (const relativePath of relativePaths) {
    const stem = path.posix.basename(relativePath, ".adoc");
    if (!stemToPaths.has(stem)) {
      stemToPaths.set(stem, []);
    }
    stemToPaths.get(stem).push(relativePath);
  }

  return [...stemToPaths.entries()]
    .filter(([, paths]) => paths.length > 1)
    .sort(([a], [b]) => comparePaths(a, b));
}

function buildRemoteUrl(relativePath) {
  const stem = path.posix.basename(relativePath, ".adoc");
  return `${OFFICIAL_USERGUIDE_ROOT}/${stem}.html`;
}

function buildRemoteLocalMap(relativePaths) {
  const repoRelativeMirrorRoot = toPosixPath(
    path.relative(repoRoot, mirrorRoot),
  );
  const groupedPaths = groupByTopLevel(relativePaths);
  const collisions = collectStemCollisions(relativePaths);
  const lines = [
    "# Remote and local Gradle userguide map",
    "",
    "This file is generated by `scripts/sync-gradle-userguide.mjs`.",
    "",
    "- Source root: user-provided absolute path passed to the sync script at generation time.",
    `- Local mirror root: \`${repoRelativeMirrorRoot}\``,
    `- Official entry page: [Gradle User Manual](${OFFICIAL_USERGUIDE_ROOT}/userguide.html)`,
    `- Official page rule: \`${OFFICIAL_USERGUIDE_ROOT}/{basename}.html\``,
    "",
    "## Notes",
    "",
    "- The local mirror keeps the upstream `userguide/` relative directory structure.",
    "- The official multi-page user guide uses file basenames for page URLs rather than the source directory layout.",
    "- URLs in this file are computed from the `.adoc` basename and are intended for lookup and cross-reference.",
    "- The sync script requires an absolute source path, but this generated file intentionally does not record the machine-specific value.",
    "- The mirrored `.adoc` files are source references only; they may include unresolved `include::`, `image::`, or other links to resources that are not mirrored here.",
    "",
    "## Summary",
    "",
    `- Mirrored ${relativePaths.length} \`.adoc\` files.`,
    collisions.length === 0
      ? "- No basename collisions were detected in the mirrored `.adoc` set."
      : `- Detected ${collisions.length} basename collision(s); see the collision section below.`,
    "",
  ];

  if (collisions.length > 0) {
    lines.push("## Basename collisions", "");
    for (const [stem, paths] of collisions) {
      lines.push(`- \`${stem}\``);
      for (const collisionPath of paths) {
        lines.push(`  - \`${collisionPath}\``);
      }
    }
    lines.push("");
  }

  lines.push("## Mapping", "");

  for (const [groupName, paths] of groupedPaths) {
    lines.push(`### ${groupName}`, "");
    for (const relativePath of paths) {
      const localMirrorPath = toPosixPath(
        path.join(repoRelativeMirrorRoot, relativePath),
      );
      lines.push(
        `- \`${relativePath}\` -> \`${localMirrorPath}\` -> [remote](${buildRemoteUrl(relativePath)})`,
      );
    }
    lines.push("");
  }

  return `${lines.join("\n")}`;
}

async function copyMirror(relativePaths, sourceRoot) {
  await fs.rm(mirrorRoot, { recursive: true, force: true });
  await fs.mkdir(mirrorRoot, { recursive: true });

  for (const relativePath of relativePaths) {
    const sourcePath = path.join(sourceRoot, relativePath);
    const destinationPath = path.join(mirrorRoot, relativePath);
    await fs.mkdir(path.dirname(destinationPath), { recursive: true });
    await fs.copyFile(sourcePath, destinationPath);
  }
}

async function writeRemoteLocalMap(relativePaths) {
  const content = buildRemoteLocalMap(relativePaths);
  await fs.writeFile(remoteLocalMapPath, content, "utf8");
}

async function main() {
  if (!sourceRootArg) {
    usage();
    process.exitCode = 1;
    return;
  }

  if (!path.isAbsolute(sourceRootArg)) {
    throw new Error(`Expected an absolute source path, got: ${sourceRootArg}`);
  }

  const sourceRoot = path.resolve(sourceRootArg);
  await assertDirectoryExists(sourceRoot);
  await fs.mkdir(referencesRoot, { recursive: true });

  const relativePaths = (await walkAdocFiles(sourceRoot))
    .map(toPosixPath)
    .sort(comparePaths);

  await copyMirror(relativePaths, sourceRoot);
  await writeRemoteLocalMap(relativePaths);

  console.log(`Mirrored ${relativePaths.length} .adoc files.`);
  console.log(
    `Local mirror: ${toPosixPath(path.relative(repoRoot, mirrorRoot))}`,
  );
  console.log(
    `Remote/local map: ${toPosixPath(path.relative(repoRoot, remoteLocalMapPath))}`,
  );
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exitCode = 1;
});
